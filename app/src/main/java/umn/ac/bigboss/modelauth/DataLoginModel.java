package umn.ac.bigboss.modelauth;

public class DataLoginModel {
    private int id;
    private String name;
    private String email;
    private int rooms;
    private String nama_kontrakan;
    private String role;
    private String alamat_sesuai_ktp;
    private String alamat_kontrakan_sekarang;
    private String harga_perbulan;
    private String foto_muka;
    private int umur;
    private String created;
    private String updated;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public int getRooms() {
        return rooms;
    }

    public void setRooms(int rooms) {
        this.rooms = rooms;
    }

    public String getNama_kontrakan() {
        return nama_kontrakan;
    }

    public void setNama_kontrakan(String nama_kontrakan) {
        this.nama_kontrakan = nama_kontrakan;
    }

    public String getRole() {
        return role;
    }

    public void setRole(String role) {
        this.role = role;
    }

    public String getAlamat_sesuai_ktp() {
        return alamat_sesuai_ktp;
    }

    public void setAlamat_sesuai_ktp(String alamat_sesuai_ktp) {
        this.alamat_sesuai_ktp = alamat_sesuai_ktp;
    }

    public String getAlamat_kontrakan_sekarang() {
        return alamat_kontrakan_sekarang;
    }

    public void setAlamat_kontrakan_sekarang(String alamat_kontrakan_sekarang) {
        this.alamat_kontrakan_sekarang = alamat_kontrakan_sekarang;
    }

    public String getHarga_perbulan() {
        return harga_perbulan;
    }

    public void setHarga_perbulan(String harga_perbulan) {
        this.harga_perbulan = harga_perbulan;
    }

    public String getFoto_muka() {
        return foto_muka;
    }

    public void setFoto_muka(String foto_muka) {
        this.foto_muka = foto_muka;
    }

    public int getUmur() {
        return umur;
    }

    public void setUmur(int umur) {
        this.umur = umur;
    }

    public String getCreated() {
        return created;
    }

    public void setCreated(String created) {
        this.created = created;
    }

    public String getUpdated() {
        return updated;
    }

    public void setUpdated(String updated) {
        this.updated = updated;
    }
}
